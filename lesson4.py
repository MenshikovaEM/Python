# Task 1 Реализовать скрипт, в котором должна быть предусмотрена функция расчета заработной платы.
# Для выполнения расчета для конкретных значений необходимо запускать скрипт с параметрами.

from sys import argv

name, hours, rate, bonus = argv
print("Выработка в часах: ", hours)
print("Ставка в час: ", rate)
print("Размер премии: ", bonus)

def salary():

    return float(hours) * float(rate) + float(bonus)

print(f"Размер заработной платы составит: {salary()}")

