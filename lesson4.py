# Task 5 Реализовать формирование списка, используя функцию range() и возможности генератора.
# В список должны войти четные числа от 100 до 1000 (включая границы).
# Необходимо получить результат вычисления произведения всех элементов списка.

from functools import reduce

def my_func(prev_el, el):
	return prev_el * el

print(f"Список четных значений от 100 до 1000: {[el for el in range(99, 1001) if el % 2 == 0]}")
print(f"Результат перемножения всех элементов списка: {reduce(my_func, [el for el in range(99, 1001) if el % 2 == 0])}")

# Task 6 Реализовать два небольших скрипта:
# а) итератор, генерирующий целые числа, начиная с указанного,
# б) итератор, повторяющий элементы некоторого списка, определенного заранее.

from itertools import count

for el in count(int(input("Введите число, с которого начать: ")), 5):
    if el > 1000:
        break
    else:
        print(el)

from itertools import cycle

my_list = [45, 3.2, False]
count = 0
for el in cycle(my_list):
    if count >= 30:
        break
    else:
        print(el)
        count += 1